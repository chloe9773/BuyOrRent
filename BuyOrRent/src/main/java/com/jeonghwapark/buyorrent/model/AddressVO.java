package com.jeonghwapark.buyorrent.model;

public class AddressVO {
	private int sid;
	private String sido;
	private int addrId;
	private String sigun;
	private String updong;
	private String li;
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSido() {
		return sido;
	}
	public void setSido(String sido) {
		this.sido = sido;
	}
	public int getAddrId() {
		return addrId;
	}
	public void setAddrId(int addrId) {
		this.addrId = addrId;
	}
	public String getSigun() {
		return sigun;
	}
	public void setSigun(String sigun) {
		this.sigun = sigun;
	}
	public String getUpdong() {
		return updong;
	}
	public void setUpdong(String updong) {
		this.updong = updong;
	}
	public String getLi() {
		return li;
	}
	public void setLi(String li) {
		this.li = li;
	}
}
